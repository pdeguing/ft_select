#include "ft_select.h"

int		main(int ac, char **av)
{
	int		i;

	if (ac < 2)
		return (display_usage());
	i = 1;
	while (i < ac)
	{
		ft_printf("%s\n", av[i]);
		i++;
	}
	return (0);
}
